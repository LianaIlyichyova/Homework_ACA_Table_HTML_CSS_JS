function absencesCount1(){
   let counter=0;
   let inputs=document.getElementsByClassName("button1")
   for(let i=0; i<inputs.length; i++){
      if(inputs[i].checked){
         counter++
      }
   }
   let res=document.getElementById("counter1");
   res.innerHTML = counter;
   if(counter>5){
      res.style = "color:red";
   }else{
      res.style = "color:#D2E4E5";
   }
}


